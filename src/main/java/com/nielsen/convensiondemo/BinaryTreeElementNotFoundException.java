package com.nielsen.convensiondemo;

public class BinaryTreeElementNotFoundException extends Exception {

	public BinaryTreeElementNotFoundException() {
	}

	public BinaryTreeElementNotFoundException(String arg0) {
		super(arg0);
	}

	public BinaryTreeElementNotFoundException(Throwable arg0) {
		super(arg0);
	}

	public BinaryTreeElementNotFoundException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public BinaryTreeElementNotFoundException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
